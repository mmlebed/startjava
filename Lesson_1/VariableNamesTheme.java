public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("\n1. Разные переменные: ");

        // цифра
        int digit = 9;
        System.out.println("Цифра = " + digit);

        // сумма чисел
        int sumNumbers = 67;
        System.out.println("Сумма чисел = " + sumNumbers);

        // произведение цифр
        int productDigits = 123;
        System.out.println("Произведение цифр = " + productDigits);

        // максимальное число
        int max = 100;
        System.out.println("Максимальное число = " + max);

        // количество десятков
        int numberTens = 60;
        System.out.println("Количество десятков = " + numberTens);

        // вес собаки
        float dogWeight = 20.1f;
        System.out.println("Вес собаки = " + dogWeight + "Кг");

        // исходное число
        int sourceNumber = 100;
        System.out.println("Исходное число = " + sourceNumber);

        // процент по вкладу
        int rate = 25;
        System.out.println("Процент по вкладу = " + rate + "%");

        // переменная хранит символ &
        char ampersand = '&';
        System.out.println("Переменная хранит символ = " + ampersand);

        // код ошибки
        String errorCode = "404";
        System.out.println("Код ошибки = " + errorCode);

        // тип сообщения
        String messageType = "txt";
        System.out.println("Тип сообщения = " + messageType);

        // число нулей
        int numberZeros = 110;
        System.out.println("Число нулей = " + numberZeros);

        // уникальное число
        int uniqueNumber = 200;
        System.out.println("Уникальное число = " + uniqueNumber);

        // случайное число
        int randomNumber = 590;
        System.out.println("Случайное число = " + randomNumber);

        // математическое выражение
        String mathExpression = "1 + 11";
        System.out.println("Математическое выражение = " + mathExpression);

        // выбор
        String choice = "Будешь кофе или чай?";
        System.out.println("Выбор (чего-либо) = " + choice);

        // счет в игре
        int score = 20;
        System.out.println("Счет (в игре) = " + score);

        // максимальная длина
        int maxLength = 120;
        System.out.println("Максимальная длина (строки) = " + maxLength);

        // пункт меню
        String menuItem = "File";
        System.out.println("Пункт меню = " + menuItem);

        // стоимость кофе на вынос
        double costCoffeeToGo = 2.67;
        System.out.println("Стоимость кофе на вынос = " + costCoffeeToGo + "руб");

        // дата начала
        String startData = "30 мая 1986";
        System.out.println("Дата начала (чего-либо) = " + startData);

        // окончание диапазона
        int endRange = 31;
        System.out.println("Окончание диапазона = " + endRange);

        // полное имя работника месяца
        String fullNameEmployeeMonth = "Иванов Иван Иванович";
        System.out.println("Полное имя работника месяца = " + fullNameEmployeeMonth);

        // заголовок электронной книги
        String ebookTitle = "Содержание";
        System.out.println("Заголовок электронной книги = " + ebookTitle);

        // размер
        String size = "XL";
        System.out.println("Размер пиджака = " + size);

        // вместимость
        int capacity = 50;
        System.out.println("Вместимость (чего-либо) = " + capacity);

        // счетчик
        int counter = 0;
        System.out.println("Счетчик = " + counter);

        // путь до файла
        String filePath = "C:/Program Files/Java/jdk-21/bin";
        System.out.println("Путь до файла = " + filePath);

        // количество чисел в каждой строке
        int numbersInRow = 95;
        System.out.println("Количество чисел в каждой строке = " + numbersInRow);

        System.out.println("\n2. boolean переменные");
        // сотни равны?
        boolean isEqualHundreds = true;
        System.out.println("Сотни равны? " + isEqualHundreds);

        // компьютер включен?
        boolean isOnPc = true;
        System.out.println("Компьютер включен? " + isOnPc);

        // есть равные цифры?
        boolean hasEqualDigits = false;
        System.out.println("Есть равные цифры? " + hasEqualDigits);

        // создано?
        boolean isCreated = true;
        System.out.println("(Что-либо) создано? " + isCreated);

        // пустое?
        boolean isEmpty = true;
        System.out.println("(Что-либо) пустое? " + isEmpty);

        // активное?
        boolean isActive = false;
        System.out.println("(Что-либо) активное? " + isActive);

        // новый?
        boolean isNew = false;
        System.out.println("Новый? " + isNew);

        // электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("Электронная почта действительная? " + isValidEmail);

        // имеются уникальные строки?
        boolean hasUniqueStrings = true;
        System.out.println("Имеются уникальные строки? " + hasUniqueStrings);

        System.out.println("\n3. Аббревиатуры");
        // старый universally unique identifier
        String oldUuid = "00112233-4455-6677-8899-aabbccddeeff";
        System.out.println("Старый UUID = " + oldUuid);

        // производитель оперативной памяти
        String ramManufacturer = "SAMSUNG";
        System.out.println("Производитель RAM = " + ramManufacturer);

        // емкость жесткого диска
        int hddCapacity = 1000000;
        System.out.println("Емкость HDD = " + hddCapacity + "Мб");

        // протокол передачи гипертекста
        String http = "HTTP";
        System.out.println("Протокол передачи гипертекста = " + http);

        // сокращенный uniform resource locator
        String shortUrl = "https://short.url/123";
        System.out.println("Сокращенный URL = " + shortUrl);

        // новый идентификатор клиента
        int newCid = 1122334455;
        System.out.println("Новый идентификатор клиента = " + newCid);

        // кодировка american standard code for information interchange
        String ascii = "ASCII";
        System.out.println("Кодировка = " + ascii);
    }
}